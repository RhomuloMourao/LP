public class Ex27Lista6 {
	
	public static void main (String[] args) {
		int controlador = 10;
			for(int a = 1; a<=10; a++){
				
				System.out.print("\n");
				System.out.print(a+ ", ");
				
				
			
				for(int b =1 ; b<= controlador ; b++){
					
					
					System.out.print(" " + b + " ");
					}
				controlador--;
				
		}
		
		
	}
}

