public class OperadoresDeAtribuicao {
	
	public static void main (String[] args) {
		
		int a = 1;
		System.out.println(a);
		a = a += 2;
		System.out.println(a);
		a = a -= 1;
		System.out.println(a);
		a = a *= 3;
		System.out.println(a);
		a = a /= 2;
		System.out.println(a);
		a = a %= 2;
		System.out.println(a);
		a++ ;
		System.out.println(a);
		a--;
		System.out.println(a);
		
	}
}

