public class Ex29Lista6 {
	
	public static void main (String[] args) {
		
		
		
		for(int a = 1 ; a<= 10 ; a++){
			
			System.out.print("\n\n");
			System.out.print(a + ", ");
			
			if (a % 2 == 0){
				for(int b = 2 ; b<=10 ; b+=2){
					System.out.print(" " + b + " ");
					
		}
			}else{
				for(int c = 1 ; c<=10 ; c+=2){
					System.out.print(" " + c + " ");
					
					
					
				}
						
			}
					
		}
			
	}
}
		
	


